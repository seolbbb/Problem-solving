dp = [[0,0,0,0] for _ in range(1000001)]

dp[1][0] = 1
dp[1][1] = 1
dp[2][0] = 1
dp[2][1] = 0
dp[2][2] = 1
dp[3][0] = 3
dp[3][1] = 1
dp[3][2] = 1
dp[3][3] = 1

for i in range(4,1000001):
    dp[i][0] = ((dp[i-1][0] + dp[i-2][0] + dp[i-3][0])
    - (dp[i-1][1] + dp[i-2][2] + dp[i-3][3])) % 1000000009
    dp[i][1] = dp[i-1][0] - dp[i-1][1] % 1000000009
    dp[i][2] = dp[i-2][0] - dp[i-2][2] % 1000000009
    dp[i][3] = dp[i-3][0] - dp[i-3][3] % 1000000009

t = int(input())
for _ in range(t):
    n = int(input())
    print(dp[n][0] % 1000000009)